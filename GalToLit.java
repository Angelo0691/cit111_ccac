public class GalToLit {
    public static void main(String args[]) {
        double gallons, liters; //Declares two separate double type variables
        
        gallons = 10;
        liters = gallons * 3.7854;
        
        System.out.println(gallons + " Gallon(s) is equivalent to " + liters + " liters");
    }
}
